package io.kar.study;

public class UnaryOperator {

	public static void main(String[] args) {
		
		int result = +1;
		
		System.out.println(result);
		
		
		result++;
		System.out.println(result);
		
		result--;
		System.out.println(result);
		
		boolean success = false;
		
		System.out.println(success);
		
		System.out.println(!success);
		
		int i = 1;
		i++;
		System.out.println(i);
		
		System.out.println(i++);
		System.out.println(i);
	}

}
